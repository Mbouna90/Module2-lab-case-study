print("4.1 The chosen number is:", number)


# -----------------------------
# 4.2 If/Else Statements
# -----------------------------
# This section demonstrates how to use if/else conditions.

if number < 5:
    print("4.2 The number is less than 5")
elif number == 5:
    print("4.2 The number is equal to 5")
else:
    print("4.2 The number is greater than 5")


# -----------------------------
# 6.1 For Loop
# -----------------------------
# This section uses a for loop to print values in a list.

